package reply.model;

public class ReplyDTO {

	private String reply_num;
	private String reply_board_num;
	private String reply_name;
	private String reply_content;

	
	public String getReply_num() {
		return reply_num;
	}
	public void setReply_num(String reply_num) {
		this.reply_num = reply_num;
	}
	public String getReply_board_num() {
		return reply_board_num;
	}
	public void setReply_board_num(String reply_board_num) {
		this.reply_board_num = reply_board_num;
	}
	public String getReply_name() {
		return reply_name;
	}
	public void setReply_name(String reply_name) {
		this.reply_name = reply_name;
	}
	public String getReply_content() {
		return reply_content;
	}
	public void setReply_content(String reply_conten) {
		this.reply_content = reply_conten;
	}

	
	
}
